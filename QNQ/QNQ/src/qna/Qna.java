package qna;

public class Qna {
	private int qnaNumber;
	private int qnaRNumber;
	private int roomNumber;
	private String anonymousID;
	private String qnaQuestion;
	
	public int getQnaRNumber() {
		return qnaRNumber;
	}

	public void setQnaRNumber(int qnaRNumber) {
		this.qnaRNumber = qnaRNumber;
	}

	

	public int getQnaNumber() {
		return qnaNumber;
	}

	public void setQnaNumber(int qnaNumber) {
		this.qnaNumber = qnaNumber;
	}

	public int getRoomNumber() {
		return roomNumber;
	}

	public void setRoomNumber(int roomNumber) {
		this.roomNumber = roomNumber;
	}

	public String getAnonymousID() {
		return anonymousID;
	}

	public void setAnonymousID(String anonymousID) {
		this.anonymousID = anonymousID;
	}

	public String getQnaQuestion() {
		return qnaQuestion;
	}

	public void setQnaQuestion(String qnaQuestion) {
		this.qnaQuestion = qnaQuestion;
	}



}
